	<h1 id = "history">history</h1>
	<a href = "http://icuhampy.tumblr.com">ICUHampy</a> started as a missed connections tumblr for Hampshire College in Jan 2012. JD DiGiovanni made it to bridge a missed connection of his own.<br><br>
	After Moderating it for a year, JD handed the reigns to Jonathan Pedigo, who built a custom website as <a href = "http://icuhampy.com/classic/">ICUHampy 2.0</a>. <br>After another year, the site was upgraded by Jonathan Pedigo to <a href = "http://icuhampy.com">ICUHampy 4.0</a> to become more than just a missed connections site incorporating secrets, drama, discussion, etc.
	<br><br>In 2014, JD and Jon recieved funding to turn ICUHampy into a buisiness. The site went through a stylistic rebrand, trashing its sketchy mysterious vibe,  and became <a href = "http://icu.university">icu.university</a> with a cute little owl for a logo. The website returned to missed connections as the main service, with the addition of "The Hole" - as a place to keep all the denied posts. It managed to expand to Amherst College, but the traffic was not at the level the team wanted it. <br><br>
	
	At the start of 2015, The buisiness then pivoted and become Matropolix, which essentially swallowed ICU whole, keeping it as one of the many services it would then provide for its communities. 
		
